			<a name="about"></a><h2><b>How mbhs.xyz works</b></h2>
			<p>mbhs.xyz is only for the use of McKinney Boyd Broncos and staff. While we are not the
			official McKinney Boyd HS website, we are going to make life easier by being the central
			web portal for all your Bronco and Red Nation needs. At the very least, our website will
			be a portal of sorts to other Bronco websites.</p>
			<p><b>Teachers and clubs/organizations can claim domain names for free</b>, which will be organized by department/interest, then
			the name of the teacher or subject. For example, "doe.eng.mbhs.xyz" would be Mr. Doe's English
			website. If the PreCalculus team decides to have one website, their address would be "precal.math.mbhs.xyz".
			These domain names will either redirect to teachers' existing websites or contain websites that
			will be maintained by the teachers themselves. Clubs/organizations can get their own domain without
			need to associate with any department. For example, TSA can use "tsa.mbhs.xyz".</p>
			<p>Students will be able to get their own domains! For example, a student named
			Jane Doe can claim janedoe.student.mbhs.xyz for her own, personal use. It will link to her
			own website that she can host elsewhere. Technical details can be discussed with the McKinney
			Boyd HS Computer Science Club.</p>
			<p>If you would like to claim a <b>vanity domain</b> we will be selling them for $5/school year.
			For example, "john.mbhs.xyz" can be claimed by anybody with a school-appropriate purpose for this domain.
			To purchase, please email <a href="mailto:jwang@mbhs.xyz">jwang@mbhs.xyz</a>, our current
			webmaster, for more information. Organizations, teachers, and other staff members do not need to pay this
			fee; only students will be required to pay for a vanity domain. Priority of names is given to organizations
			and teachers.</p>
			<p>We will try to get news and announcements here as well.</p>
			<p><b>We are currently only taking requests from teachers, other staff members, school organizations,
			and students interested in buying vanity domains.</b>
			Please email <a href="mailto:jwang@mbhs.xyz">jwang@mbhs.xyz</a> so you can get a domain at mbhs.xyz!</p>
